    private void bComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bComprobarActionPerformed
       //pone a 0 el valor del campo aciertos, invoca al método colorearEinformar 
        //con cada JTextfield del panel jpRespuestas, su correspondiente JLabel 
        //del panel jpRespuestasCorrectas y el número de línea de la tabla Cuando 
        //acaba de colorearEinformar, si aciertos es mayor o igual que 5 se habilitará el botón bBorrar
        this.aciertos=0;
        Component[] numerosCorrectos=jpRespuestasCorrectas.getComponents();
        Component[] numerosEscritos=jpRespuestas.getComponents();
        for(int x=0;x<10;x++){
            JLabel eti=(JLabel) numerosCorrectos[x];
            JTextField ct=(JTextField) numerosEscritos[x];
            this.colorearEinformar(ct, eti, x);
        }
        if(aciertos>=5){
          bBorrar.setEnabled(true);
        }
    }//GEN-LAST:event_bComprobarActionPerformed

    private void bBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBorrarActionPerformed
        // Borra el texto de todos los JtextFiel del panel jpRespuestas y de todos 
        //los Jlabel del panel jpRespuestasCorrectas y después invoca al método pedirNumero()
        this.aciertos=0;
        Component[] numerosCorrectos=jpRespuestasCorrectas.getComponents();
        Component[] numerosEscritos=jpRespuestas.getComponents();
        for(int x=0;x<10;x++){
            JLabel eti=(JLabel) numerosCorrectos[x];
            JTextField ct=(JTextField) numerosEscritos[x];
            eti.setText("");
            ct.setText("");
            ct.setBackground(Color.white);
         }
        this.pedirNumero();
    }//GEN-LAST:event_bBorrarActionPerformed

