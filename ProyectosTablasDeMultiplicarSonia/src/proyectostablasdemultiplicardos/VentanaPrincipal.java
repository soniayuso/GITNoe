//comprueba si en el JtextField ct el usuario ha escrito la respuesta 
//correcta, si no ha sido así el fondo del ct pasará a colorearse en rojo y en
//eti se escribirá la respuesta correcta, y si la respuesta es correcta 
//se incrementará el número de aciertos, se coloreará el fondo de ct en verde y
//se borrará el texto de eti

private void colorearEinformar(JTextField ct, JLabel eti, int n){
 int numero=Integer.parseInt(ct.getText());
    
    if(!gestora.comprueba(numero, n)){
        ct.setBackground(Color.red);
        eti.setText(""+gestora.devuelveCorrecta(n));
        }
    else{
        ct.setBackground(Color.green);
        this.aciertos=aciertos+1;
     
 }
}
//comprueba que el usuario ha introducido un némero entre 2 y 9 
//(ambos incluidos) en ctNumeroTabla, si no lo ha hecho se genera una excepción
//con un mensaje adecuado
private void comprobarEntrada()throws Exception{
    String elNumero=ctNumeroDeTabla.getText();
if(!elNumero.matches("[2-9]{1}")){
    throw new Exception("inroduce un nuúmero adecuado");
}
}
//invoca al método setVisible de todos los paneles para que se visualicen o no, 
//        dependiendo de que el parámetro mostrar que recibe sea true o false
private void mostrarPaneles(boolean mostrar){
    jpPreguntas.setVisible(mostrar);
    jpRespuestas.setVisible(mostrar);
    jpRespuestasCorrectas.setVisible(mostrar);
    jpBotones.setVisible(mostrar);
}
//invoca al método mostrarPaneles para que los paneles NO se visualicen, 
//cambia el tamaño de la ventana a 400 x 200 píxeles, habilita ctNumeroTable y 
//bAceptar para que el usuario pueda introducir el número de tabla con el 
//que desea trabajar
private void pedirNumero(){
     mostrarPaneles(false);
    setSize(400, 200);
    ctNumeroDeTabla.setEnabled(true);
    bAceptar.setEnabled(true);
}

//Se deshabilitarán bAceptar y ctNumeroTabla para que el usuario no pueda introducir 
//un nuevo número de tabla y también se inhabilitará bBorrar para que no pueda borrar 
//la información que ya ha introducido en la tabla, se establecerá como tamaño de la
//ventana 400 pixeles de ancho por 500 de alto, se eliminarán los componentes actuales 
//de jpPreguntas, jpRespuestas y jpRespuestasCorrectas y se generarán los Jlabel,
//JTextField correctos y correspondientes a los paneles anteriores, por último, 
//se invocará al método mostrarPaneles para que los paneles se visualicen
private void visualizarPaneles(){
     bAceptar.setEnabled(false);
    ctNumeroDeTabla.setEnabled(false);
    bBorrar.setEnabled(false);
    setSize(400, 500);
    jpPreguntas.removeAll();
    jpRespuestas.removeAll();
    jpRespuestasCorrectas.removeAll();
    
    
    for(int x=0;x<10;x++){
        int numero=x+1;
        String mensaje="         "+this.ctNumeroDeTabla.getText()+"x"+numero;
        JLabel unLaberlPreguntas=new JLabel(mensaje);
        JLabel unLabel=new JLabel();
        JTextField unField= new JTextField();
        unField.addFocusListener(this);
        jpPreguntas.add(unLaberlPreguntas);
        jpRespuestas.add(unField);
        jpRespuestasCorrectas.add(unLabel);
        }
      mostrarPaneles(true);     
}


